#Desenvolva um programa que use um laço for para fazer uma contagem regressiva de 10 até 1 e, em seguida, exiba "Feliz Ano Novo!".

#1. Loop for, para repetir a iteração. Variável "counter" para armazenar as iterações. Operador de associação in para verificar o conteúdo de range(), com start em 10, stop em 0 e step em -1.
#2. Função print para mostrar cada iteração.
#3. Função print identada para trás, para mostrar a mensagem "Feliz Ano Novo!"

for counter in range(10, 0, -1):
    print(counter)
print("Feliz Ano Novo!")