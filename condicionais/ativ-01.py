#Comparação de Idades:
#Peça ao usuário duas idades e use operadores de comparação para verificar se a primeira idade é maior, menor ou igual à segunda.

# informações do tipo inteiro solicitadas ao usuário através da função input
idade_1 = int(input("Insira sua idade: "))
idade_2 = int(input("Insira a idade do seu irmão: "))

#função print, usada para mostrar no terminal o resultado
#1ª - se idade_1 é maior que idade_2
#2ª - se idade_1 é menor que idade_2
#3ª - se idade_1 é igual à idade_2
print(idade_1 > idade_2)
print(idade_1 < idade_2)
print(idade_1 == idade_2)