#Verificar Igualdade de Strings:
#Peça ao usuário duas palavras e use operadores de comparação para verificar se elas são iguais.

# informações do tipo string solicitadas ao usuário através da função input
# .capitalize() deixe a primeira letra das palavras em maiúcula
palavra_1 = str(input("Insira sua palavra favorita: ")) .capitalize()
palavra_2 = str(input("Insira a palavra que menos gosta: ")) .capitalize()

# função print mostra no terminal se palavra_1 é igual a palavra_2
print(palavra_1 == palavra_2)

