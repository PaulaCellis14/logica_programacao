#Dobro do Valor:
#Solicite ao usuário um número e use o operador de atribuição *= para dobrar o valor e exibir o resultado.

# informações do tipo int solicitadas ao usuário através da função input
num = int(input("Me informe um número: "))

#operação que multiplica, ataves do operador *, o numero inserido pelo usuário por dois
num *= 2

#função print mostra o resultado do calculo.
print(num)